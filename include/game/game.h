typedef struct {
  int call_every_ms; // should be mutiple of 10
  void (*handler)();
  volatile int counter;
} Event;

// https://tetris.wiki/Tetromino
typedef enum {
  SHAPE_I,
  SHAPE_O,
  SHAPE_T,
  SHAPE_S,
  SHAPE_Z,
  SHAPE_J,
  SHAPE_L
} Shape;

typedef enum {
  CYAN, // light blue
  YELLOW,
  PURPLE,
  GREEN,
  RED,
  BLUE,
  ORANGE,
  CLEAR
} Color;

// initial 0 angle for each piece will look like this: https://tetris.wiki/Tetromino
typedef enum {
  ANGLE_0,
  ANGLE_90,
  ANGLE_180,
  ANGLE_270
} Angle;

typedef struct {
  int x;
  int y;
} Point;

// dynamic piece in the board (should be one piece existed at a time)
typedef struct {
  Shape shape;
  Color color;
  Angle angle;
  Point center_point; // position of the center point, this is x, y position in the 2D array
} Piece;

// static block that have settle down (generated by the previous dynamic pieces)
typedef struct {
  Color color;
} Block;

typedef struct 
{
    const char* item_string;
    int x;
    int y;
    void (*action)(void);
    struct menu_item *sub_menu;
}menu_item;

